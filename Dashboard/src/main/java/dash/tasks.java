package dash;

public class tasks {

	private int dashID;
	private String dashTitle;
	private String dashDate;
	private String dashContent;
	private int dashAvailable;
	private String dashDifficulty;
	private String dashCheck;

	public int getDashID() {
		return dashID;
	}

	public void setDashID(int dashID) {
		this.dashID = dashID;
	}

	public String getDashTitle() {
		return dashTitle;
	}

	public void setDashTitle(String dashTitle) {
		this.dashTitle = dashTitle;
	}

	public String getDashDate() {
		return dashDate;
	}

	public void setDashDate(String dashDate) {
		this.dashDate = dashDate;
	}

	public String getDashContent() {
		return dashContent;
	}

	public void setDashContent(String dashContent) {
		this.dashContent = dashContent;
	}

	public int getDashAvailable() {
		return dashAvailable;
	}

	public void setDashAvailable(int dashAvailable) {
		this.dashAvailable = dashAvailable;
	}

	public String getDashDifficulty() {
		return dashDifficulty;
	}

	public void setDashDifficulty(String dashDifficulty) {
		this.dashDifficulty = dashDifficulty;
	}
	
	public String getDashCheck() {
		return dashCheck;
	}

	public void setDashCheck(String dashCheck) {
		this.dashCheck = dashCheck;
	}


}
